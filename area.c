#include<stdio.h>
 int main()
 {
 	int length, breadth, area, perimeter;
 	printf("Enter the length:");
 	scanf("%d",&length);
 	printf("Enter the breadth:");
 	scanf("%d",&breadth);
 	area : length*breadth;
 	printf("Area of Rectangle:%d %n",area);
 	perimeter : 2*length+breadth;
 	printf("perimeter of Rectangle:%d %n",perimeter);
 	return 0;
}